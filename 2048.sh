################################################################################
## READ ACTION
################################################################################
_read_action()
{
    local input=""
    read -sn 1 -t 1 input 
    echo $input
}

################################################################################
## MAIN
################################################################################
while :
do
    action=`_read_action`

    case $action in 
        W | w)
            echo "UP"
            ;;
        S | s)
            echo "DOWN"
            ;;
        A | a)
            echo "LEFT"
            ;;
        D | d)
            echo "RIGHT"
            ;;
        Q | q)
            echo "QUIT"
            break
            ;;
        *)
            echo "HEHE"
            ;;
    esac

done
